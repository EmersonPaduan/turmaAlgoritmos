#include <stdio.h>

int main()
{
    // Operadores aritméticos: + - * /

    // resto da divisão %
    printf("%d\n", 7 / 5);
    printf("%d\n", 7 % 5);

    printf("%f\n", 7.0 / 5);

    // Operadores relacionais: >, >=, <, <=, ==, != 
    // TRUE (qq valor > 0) / FALSE (zero)

    printf("\n6 > 5 ? %d\n", 6 > 5);
    printf("6 <= 5 ? %d\n", 6 <= 5);
    printf("6 = 5 + 1 ? %d\n", 6 ==  5 + 1);

    return 0;
}

